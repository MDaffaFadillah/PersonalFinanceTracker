private void loadTable() {
    DefaultTableModel model = (DefaultTableModel) tblBudget.getModel();
    model.setRowCount(0); // Menghapus isi tabel sebelum memuat ulang
    
    // Pastikan ID User diambil dari Session
    int idUser = Session.getIdUser(); 
    
    // Panggil DAO yang mengambil semua budget milik user tersebut
    // Jika Anda punya method khusus filter bulan, pastikan parameternya sesuai (sekarang di DB adalah bulan 1)
    List<Budget> list = budgetDAO.getAllBudgetByUser(idUser); 
    
    for (Budget b : list) {
        model.addRow(new Object[]{
            b.getNamaKategori(), 
            b.getLimitBudget(), 
            b.getBulan(), 
            b.getTahun()
        });
    }
}
private void loadKategoriKeCombo() {
    cmbKategori.removeAllItems();
    KategoriDAO kDAO = new KategoriDAO(); // Pastikan sudah diinstansiasi
    List<Kategori> list = kDAO.getAllKategori();
    
    for (Kategori k : list) {
        cmbKategori.addItem(k); // Memasukkan objek Kategori langsung
    }
    }
    
