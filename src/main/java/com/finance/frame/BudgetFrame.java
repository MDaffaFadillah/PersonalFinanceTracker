    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        // 1. Ambil objek Kategori yang terpilih di ComboBox
        Object selectedItem = cmbKategori.getSelectedItem();
        if (selectedItem instanceof Kategori) {
            Kategori k = (Kategori) selectedItem;
            
            // 2. Ambil nilai dari input field
            double limit = Double.parseDouble(txtLimitBudget.getText());
            int bulan = Integer.parseInt(txtBulan.getText());
            int tahun = Integer.parseInt(txtTahun.getText());

            // 3. Gunakan constructor kosong, lalu isi dengan setter
            Budget b = new Budget();
            b.setIdUser(1); // Sementara (kita akan urus Session nanti)
            b.setIdKategori(k.getIdKategori()); // Ambil ID Kategori dari objek
            b.setLimitBudget(limit);
            b.setBulan(bulan);
            b.setTahun(tahun);

            // 4. Simpan ke database
            if (budgetDAO.tambahBudget(b)) {
                JOptionPane.showMessageDialog(this, "Budget berhasil disimpan!");
                loadTable();
                txtLimitBudget.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan ke database.");
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format angka salah (Nominal/Bulan/Tahun harus angka).");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

