    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (txtNamaKategori.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kategori tidak boleh kosong!");
            return;
        }

        Kategori k = new Kategori();
        k.setNamaKategori(txtNamaKategori.getText());
        k.setJenis(cmbJenis.getSelectedItem().toString());
        
        if (kategoriDAO.tambahKategori(k)) {
            JOptionPane.showMessageDialog(this, "Kategori berhasil ditambahkan!");
            loadTable();
            txtNamaKategori.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan kategori.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblKategoriMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKategoriMouseClicked
        int row = tblKategori.getSelectedRow();
    if (row != -1) {
        // Mengisi field dari baris yang dipilih
        txtNamaKategori.setText(tblKategori.getValueAt(row, 1).toString());
        cmbJenis.setSelectedItem(tblKategori.getValueAt(row, 2).toString());
    }
    }//GEN-LAST:event_tblKategoriMouseClicked

