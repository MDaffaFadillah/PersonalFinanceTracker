    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblKategori.getModel();
        model.setRowCount(0);
        List<Kategori> list = kategoriDAO.getAllKategori();
        for (Kategori k : list) {
            model.addRow(new Object[]{k.getIdKategori(), k.getNamaKategori(), k.getJenis()});
        }
    }

