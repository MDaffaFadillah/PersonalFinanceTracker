    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblKategori.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit!");
        return;
    }
    
    int id = (int) tblKategori.getValueAt(row, 0);
    Kategori k = new Kategori(id, txtNamaKategori.getText(), cmbJenis.getSelectedItem().toString());
    
    if (kategoriDAO.editKategori(k)) {
        JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
        loadTable();
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblKategori.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
        return;
    }
    
    int confirm = JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        int id = (int) tblKategori.getValueAt(row, 0);
        if (kategoriDAO.hapusKategori(id)) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            loadTable();
            txtNamaKategori.setText("");
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

