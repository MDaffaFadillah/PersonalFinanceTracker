private void loadTableTransaksi() {
        DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
    model.setRowCount(0);
    int idUser = Session.getIdUser();   

    List<Transaksi> list = transaksiDAO.getTransaksiByUser(idUser);
    for (Transaksi t : list) {
        // Sesuaikan urutan dengan header tabel Anda: ID, Tanggal, Nominal, Keterangan
        model.addRow(new Object[]{
            t.getIdTransaksi(), 
            t.getTanggal(),     
            t.getNominal(),     
            t.getKeterangan() 
        });
    }
        tblTransaksi.setModel(model);
    }

    private void loadKategoriToCombo() {
        cmbKategori.removeAllItems();   
        List<Kategori> list = kategoriDAO.getAllKategori();
        for (Kategori k : list) {
            cmbKategori.addItem(k);
        }
    }
