    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         try {
        Transaksi t = new Transaksi();
        
        // MENGUBAH: Mengambil ID User dari Session yang sedang aktif
        t.setIdUser(Session.getIdUser()); 
        
        Kategori k = (Kategori) cmbKategori.getSelectedItem();
        t.setIdKategori(k.getIdKategori());
        t.setNominal(Double.parseDouble(txtNominal.getText()));
        t.setKeterangan(txtKeterangan.getText());
        t.setTanggal(Date.valueOf(txtTanggal.getText()));

        if (k.getJenis().equalsIgnoreCase("Pengeluaran")) {
            // MENGUBAH: Ambil ID user dan bulan/tahun dinamis dari Session/System
            int idUser = Session.getIdUser();
            // Catatan: Gunakan fungsi untuk mendapatkan bulan/tahun saat ini dari sistem
            // Agar tidak hardcode "7" dan "2026"
            int bulan = java.time.LocalDate.now().getMonthValue();
            int tahun = java.time.LocalDate.now().getYear();
            
            Budget b = budgetDAO.cekBudget(idUser, k.getIdKategori(), bulan, tahun);
            
            if (b != null && (b.getTotalTerpakai() + t.getNominal() > b.getLimitBudget())) {
                JOptionPane.showMessageDialog(this, "PERINGATAN: Budget " + k.getNamaKategori() + " terlampaui!");
            }
        }

        if (transaksiDAO.tambahTransaksi(t)) {
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            loadTableTransaksi();
            txtNominal.setText("");
            txtKeterangan.setText("");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

